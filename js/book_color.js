$('#bookcolor').change(function () {
    if ($(this).val() == 'blue') {
        $('#preview-book').attr('src', './img/notebook_blue.png');
    }
    if ($(this).val() == 'bluegreen') {
        $('#preview-book').attr('src', './img/notebook_bluegreen.png');
    }
    if ($(this).val() == 'brown') {
        $('#preview-book').attr('src', './img/notebook_brown.png');
    }
    if ($(this).val() == 'green') {
        $('#preview-book').attr('src', './img/notebook_green.png');
    }
    if ($(this).val() == 'indigo') {
        $('#preview-book').attr('src', './img/notebook_indigo.png');
    }
    if ($(this).val() == 'lightblue') {
        $('#preview-book').attr('src', './img/notebook_lightblue.png');
    }
    if ($(this).val() == 'lime') {
        $('#preview-book').attr('src', './img/notebook_lime.png');
    }
    if ($(this).val() == 'pink') {
        $('#preview-book').attr('src', './img/notebook_pink.png');
    }
    if ($(this).val() == 'red') {
        $('#preview-book').attr('src', './img/notebook_red.png');
    }
    if ($(this).val() == 'yellow') {
        $('#preview-book').attr('src', './img/notebook_yellow.png');
    }
});